'''
You are given the number of apples and oranges in a basket. If the number of apples is greater than the number of oranges, print "More apples". If the number of oranges is greater, print "More oranges". If they are equal, print "Equal".
'''
apples = int(input())
oranges = int(input())

if (apples > oranges):
    print("More apples")
elif (apples < oranges):
    print("More oranges")
else:
    print("Equal")
